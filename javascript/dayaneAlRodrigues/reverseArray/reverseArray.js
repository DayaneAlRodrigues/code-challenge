function reverseArray(array){
    return array.reverse();
}
const array1 = [1, 2, 3, 4];
const array2 = [9, 9, 2, 3, 4];


console.log(`Essa função irá reverter a ordem do array ${array1} para ${reverseArray(array1)}.`)
console.log(`Essa função irá reverter a ordem do array ${array2} para ${reverseArray(array2)}.`)
